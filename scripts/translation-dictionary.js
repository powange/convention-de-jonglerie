// Fonction helper pour obtenir toutes les traductions dans un objet plat
export function getFlatTranslations() {
  const flat = {}

  for (const category of Object.values(TRANSLATION_DICTIONARY)) {
    Object.assign(flat, category)
  }

  return flat
}
